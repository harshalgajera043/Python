#type casting
name_entry = len(input("what is your name?"))

#type_casting_to_string
Name_entry = str(name_entry)
print(type(Name_entry))
print("your name has " + Name_entry + " charecters" )

#type_casting_to_Boolean
Name_entry_new = bool(name_entry)
print(type(Name_entry_new))
print(Name_entry_new)

#type_casting_to_float
New_Name_entry = float(name_entry)
print(New_Name_entry)
print(type(New_Name_entry))